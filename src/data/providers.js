/** Filter providers by selected insurance and category (specialty). */
export function filterProviders(providers, selectedInsurance, selectedCategory) {
  if (!providers?.length) return [];
  let result = providers;
  if (selectedInsurance && selectedInsurance.name !== 'No Insurance') {
    result = result.filter((p) => p.accepts?.includes(selectedInsurance.name));
  }
  if (selectedCategory && selectedCategory !== 'All Categories') {
    result = result.filter((p) => p.specialty === selectedCategory);
  }
  return result;
}

